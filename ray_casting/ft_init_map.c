#include "../cub3d.h"

int	ft_init_map(t_data *img)
{
	int	i;
	int	j;

	i = 0;
	while (img->map[i])
	{
		j = 0;
		while (img->map[i][j])
		{
			ft_set_player(img, i, j);
			j++;
		}
		i++;
	}
	return (0);
}
